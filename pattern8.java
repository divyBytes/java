public class pattern8 {
    public static void main(String[] args) {
        int n = 7;
        while(n >= 4)
        {for(int i = 4; i >= 1; i--){
            for(int j = 1; j <= i; j++){
                System.out.print("*");
            }
        System.out.println();
        n--;
        }
        }
        for(int i = 2; i <= 4; i++){
            for(int j = 1 ; j <= i ; j++){
                System.out.print("*");
            }
            System.out.println();
        }
        }
        
    }

    

