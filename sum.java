public class sum {
    public static void main(String[] args) {
        int a = 10;
        int b = 23;
        int sum =  a+b;
        System.out.println("sum is "+sum);

        int diff = b - a;
        System.out.println("difference is "+diff);

        int mul = a * b;
        System.out.println("multiply is "+mul);

        float div = b / a;
        System.out.println("division is "+div);
    }
}
